    private void btnPotenciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPotenciarActionPerformed
        primerNumero = Double.parseDouble(cadenaNumeros);
        lblMemoria.setText("sqr("+cadenaNumeros+")");
        resultado = primerNumero*primerNumero;
        String resultadoTexto;
        if(resultado == Math.floor(resultado)){
            resultadoTexto=String.format("%d", (int) resultado);
        }else{
            resultadoTexto=String.format("%.2f", resultado);
        }
        txtResultado.setText(resultadoTexto);
        cadenaNumeros = String.valueOf(resultadoTexto);
    }//GEN-LAST:event_btnPotenciarActionPerformed


