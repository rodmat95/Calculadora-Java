    private void btnOperarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOperarActionPerformed
        double segundoNumero;
        if(operacion.equals("nula")){
            txtResultado.setText(cadenaNumeros);
        }
        else if(operacion.equals("sumar")){
            segundoNumero = Double.parseDouble(cadenaNumeros);
            String primerNumeroTexto;
            if (primerNumero == Math.floor(primerNumero)){
                primerNumeroTexto = String.format("%d", (int) primerNumero);
            }else{
                primerNumeroTexto = String.valueOf(primerNumero);
            }
            lblMemoria.setText((primerNumeroTexto+" "+operador+" "+cadenaNumeros)+" =");
            resultado = primerNumero + segundoNumero;
            /*
            String resultadoTexto;
            if (resultado % 1 == 0){
                resultadoTexto = String.format("%d", (int) resultado);
            }else{
                resultadoTexto = String.valueOf(resultado);
            }
            lblResultado.setText(resultadoTexto);
            */
            if (resultado == Math.floor(resultado)){
                txtResultado.setText(String.format("%d", (int) resultado));
            }else{
                txtResultado.setText(String.format("%.2f", resultado));
            }
            cadenaNumeros = String.valueOf(resultado);
            operacion = "nula";
        }
        else if(operacion.equals("restar")){
            segundoNumero = Double.parseDouble(cadenaNumeros);
            String primerNumeroTexto;
            if (primerNumero == Math.floor(primerNumero)){
                primerNumeroTexto = String.format("%d", (int) primerNumero);
            }else{
                primerNumeroTexto = String.valueOf(primerNumero);
            }
            lblMemoria.setText((primerNumeroTexto+" "+operador+" "+cadenaNumeros)+" =");
            resultado = primerNumero - segundoNumero;
            /*
            String resultadoTexto;
            if (resultado % 1 == 0){
                resultadoTexto = String.format("%d", (int) resultado);
            }else{
                resultadoTexto = String.valueOf(resultado);
            }
            lblResultado.setText(resultadoTexto);
            */
            if (resultado == Math.floor(resultado)){
                txtResultado.setText(String.format("%d", (int) resultado));
            }else{
                txtResultado.setText(String.format("%.2f", resultado));
            }
            cadenaNumeros = String.valueOf(resultado);
            operacion = "nula";
        }
        else if(operacion.equals("multiplicar")){
            segundoNumero = Double.parseDouble(cadenaNumeros);
            String primerNumeroTexto;
            if (primerNumero == Math.floor(primerNumero)){
                primerNumeroTexto = String.format("%d", (int) primerNumero);
            }else{
                primerNumeroTexto = String.valueOf(primerNumero);
            }
            lblMemoria.setText((primerNumeroTexto+" "+operador+" "+cadenaNumeros)+" =");
            resultado = primerNumero * segundoNumero;
            /*
            String resultadoTexto;
            if (resultado % 1 == 0){
                resultadoTexto = String.format("%d", (int) resultado);
            }else{
                resultadoTexto = String.valueOf(resultado);
            }
            lblResultado.setText(resultadoTexto);
            */
            if (resultado == Math.floor(resultado)){
                txtResultado.setText(String.format("%d", (int) resultado));
            }else{
                txtResultado.setText(String.format("%.2f", resultado));
            }
            cadenaNumeros = String.valueOf(resultado);
            operacion = "nula";
        }
        else if (operacion.equals("dividir")) {
            segundoNumero = Double.parseDouble(cadenaNumeros);
            if (segundoNumero == 0) {
                txtResultado.setText("No se puede dividir entre cero");
            }else{
                String primerNumeroTexto;
                if (primerNumero == Math.floor(primerNumero)){
                    primerNumeroTexto = String.format("%d", (int) primerNumero);
                }else{
                    primerNumeroTexto = String.valueOf(primerNumero);
                }
                lblMemoria.setText((primerNumeroTexto+" "+operador+" "+cadenaNumeros)+" =");
                resultado = primerNumero / segundoNumero;
                /*
                String resultadoTexto;
                if (resultado % 1 == 0){
                    resultadoTexto = String.format("%d", (int) resultado);
                }else{
                    resultadoTexto = String.valueOf(resultado);
                }
                lblResultado.setText(resultadoTexto);
                */
                if (resultado == Math.floor(resultado)){
                    txtResultado.setText(String.format("%d", (int) resultado));
                }else{
                    txtResultado.setText(String.format("%.2f", resultado));
                }
                cadenaNumeros = String.valueOf(resultado);
                operacion = "nula";
            }
        }
        
        //lblMemoria.setText("");
        activado = true;
        punto = false;
    }//GEN-LAST:event_btnOperarActionPerformed

